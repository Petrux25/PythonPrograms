
#Function : BinToDecimal
#Input: Binary string number
#Output: decimal value of the string
#E.g
# >>> BinToDecimal("1010")
# >>> 10

def BinToDecimal(BinNumber):
    Decimal = int(BinNumber,2) #Converts string to
    return Decimal             #int. 2 is the base

